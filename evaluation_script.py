# ================== AUTH FUNCTIONS ==================

def refresh_access_token():
    """‡∏î‡∏∂‡∏á access token ‡πÉ‡∏´‡∏°‡πà‡∏à‡∏≤‡∏Å refresh token"""
    global ACCESS_TOKEN
    
    if not REFRESH_TOKEN or REFRESH_TOKEN == "YOUR_REFRESH_TOKEN_HERE":
        print("‚ö†Ô∏è No REFRESH_TOKEN set, cannot refresh")
        return False
    
    try:
        resp = requests.post(
            f"{AUTH_BASE}/auth/refresh",
            cookies={"refresh_token": REFRESH_TOKEN}
        )
        
        if resp.status_code == 200 or resp.status_code == 201:
            ACCESS_TOKEN = resp.cookies.get("access_token", "")
            if not ACCESS_TOKEN:
                data = resp.json()
                ACCESS_TOKEN = data.get("accessToken") or data.get("access_token", "")
            print(f"üîÑ Token refreshed successfully")
            return True
        else:
            print(f"‚ùå Refresh failed: {resp.status_code} - {resp.text}")
            return False
            
    except Exception as e:
        print(f"‚ùå Refresh error: {e}")
        return False

def get_headers():
    """‡∏™‡∏£‡πâ‡∏≤‡∏á headers ‡∏û‡∏£‡πâ‡∏≠‡∏° token"""
    if ACCESS_TOKEN:
        return {"Authorization": f"Bearer {ACCESS_TOKEN}"}
    return {}

# ================== HELPER FUNCTIONS ==================

def extract_video_id(url: str) -> str:
    """‡∏î‡∏∂‡∏á video ID ‡∏à‡∏≤‡∏Å YouTube URL"""
    if "v=" in url:
        return url.split("v=")[1].split("&")[0]
    elif "youtu.be/" in url:
        return url.split("youtu.be/")[1].split("?")[0]
    return url

def wait_for_summary(job_id: str, timeout: int = 600) -> dict:
    """‡∏£‡∏≠‡∏à‡∏ô summarization ‡πÄ‡∏™‡∏£‡πá‡∏à"""
    start_time = time.time()
    
    while time.time() - start_time < timeout:
        try:
            response = requests.get(f"{API_BASE}/summary/{job_id}", headers=get_headers())
            
            # ‡∏ñ‡πâ‡∏≤ 401 ‡∏•‡∏≠‡∏á refresh token
            if response.status_code == 401:
                if refresh_access_token():
                    continue
            
            if response.status_code == 200:
                data = response.json()
                status = data.get("status", "")
                percent = data.get("percent", 0)
                
                if status == "DONE" and data.get("summary"):
                    return data
                elif status in ["FAILED", "ERROR", "CANCEL"]:
                    raise Exception(f"Job failed with status: {status}")
                
                print(f"   ‚è≥ {status} - {percent}%", end="\r")
                
            time.sleep(5)
        except Exception as e:
            print(f"Error checking status: {e}")
            time.sleep(5)
    
    raise TimeoutError(f"Timeout waiting for job {job_id}")

def get_summary_from_api(youtube_url: str, whisper_temp: float) -> str:
    """‡πÄ‡∏£‡∏µ‡∏¢‡∏Å API ‡∏™‡∏£‡∏∏‡∏õ‡∏Ç‡∏≠‡∏á‡∏£‡∏∞‡∏ö‡∏ö"""
    print(f"  Calling API with temp={whisper_temp}...")
    
    try:
        # ‡∏™‡∏£‡πâ‡∏≤‡∏á job
        response = requests.post(
            f"{API_BASE}/summary",
            json={
                "youtubeUrl": youtube_url,
                "whisperTemp": whisper_temp
            },
            headers=get_headers(),
            timeout=30
        )
        
        # ‡∏ñ‡πâ‡∏≤ 401 ‡∏•‡∏≠‡∏á refresh token ‡πÅ‡∏•‡πâ‡∏ß‡∏•‡∏≠‡∏á‡πÉ‡∏´‡∏°‡πà
        if response.status_code == 401:
            if refresh_access_token():
                response = requests.post(
                    f"{API_BASE}/summary",
                    json={
                        "youtubeUrl": youtube_url,
                        "whisperTemp": whisper_temp
                    },
                    headers=get_headers(),
                    timeout=30
                )
        
        if response.status_code != 200 and response.status_code != 201:
            print(f"  Error: {response.status_code} - {response.text}")
            return ""
        
        data = response.json()
        job_id = data.get("jobId")
        
        # ‡∏ñ‡πâ‡∏≤ cached ‡∏Å‡πá‡∏î‡∏∂‡∏á‡πÄ‡∏•‡∏¢
        if data.get("status") == "CACHED" or data.get("fromCache"):
            print(f"  Found cached result")
            result = requests.get(f"{API_BASE}/summary/{job_id}", headers=get_headers())
            return result.json().get("summary", "")
        
        # ‡∏£‡∏≠‡∏à‡∏ô‡πÄ‡∏™‡∏£‡πá‡∏à
        print(f"  Waiting for job {job_id}...")
        result = wait_for_summary(job_id)
        return result.get("summary", "")
        
    except Exception as e:
        print(f"  Error: {e}")
        return ""

